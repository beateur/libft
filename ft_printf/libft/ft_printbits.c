#include "libft.h"

void	ft_printbits(int c)
{
	int	i;

	i = 0;
	while (i < 16)
	{
		ft_putnbr(c % 2);
		c = c >> 1;
		if (i % 4 == 3)
			ft_putchar('\n');
		i++;
	}
}
